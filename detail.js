// Вспомогательные функции
function getRarityName(rarity) {
    const names = {
        'common': 'ОБЫЧНАЯ',
        'rare': 'РЕДКАЯ',
        'epic': 'ЭПИЧЕСКАЯ',
        'legendary': 'ЛЕГЕНДАРНАЯ',
        'champion': 'ЧЕМПИОН'
    };
    return names[rarity] || rarity.toUpperCase();
}